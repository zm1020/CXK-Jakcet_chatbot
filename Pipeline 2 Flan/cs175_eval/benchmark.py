QUERY_CATEGORIES = {
    "travel": ["travel", "trip"],
    "hiking": ["hiking", "trail", "outdoor"],
    "extreme_cold": ["parka", "extreme cold", "freezing", "winter"],
    "vest": ["vest"],
    "hoody": ["hoody", "hoodie"],
    "gender_specific": ["women", "men"],
}

def query_category(query: str) -> str:
    ql = query.lower()
    if "travel" in ql or "trip" in ql:
        return "travel"
    if "hiking" in ql or "trail" in ql or "outdoor" in ql:
        return "hiking"
    if "parka" in ql or "extreme cold" in ql or "freezing" in ql or "winter" in ql:
        return "extreme_cold"
    if "vest" in ql:
        return "vest"
    if "hoody" in ql or "hoodie" in ql:
        return "hoody"
    if "women" in ql or "men" in ql:
        return "gender_specific"
    return "everyday"
